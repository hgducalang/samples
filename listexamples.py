#declaring list
fruits = ["apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"]
print(fruits)

#printing a specified item in a list using index

#positive index always starts with 0 and from left to right
print(fruits[0])

#negative index always starts with -1 and from right to left
print(fruits[-1])

#printing a specified items from start index to end index
print(fruits[2:5])
print(fruits[:3])
print(fruits[2:])

#checking the length or number of items in a list
print(len(fruits))